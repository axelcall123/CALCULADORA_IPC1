        NOSE ns=new NOSE();
        String GR=GradRad.getSelectedItem().toString();
        double NumeroX=Double.parseDouble(Tnumero.getText());
        double pi = 3.14159265358979323846;
        if(GR.equals("Radianes")){
            if(NumeroX==1.570796327){JOptionPane.showMessageDialog(null, "INFINITO");}else{
            if(NumeroX < -pi * 2){
                Res.setText(ns.decimales(ns.CosSen(ns.Disminuir(NumeroX), 1)/ns.CosSen(ns.Disminuir(NumeroX), 0)));//ENVIA EL NUMERO EN RAD Y 1 ES SEN
            } 
            else if (pi * 2 >= NumeroX) {//VE SI SE PARAS DE 2PI
                Res.setText(ns.decimales(ns.CosSen(NumeroX, 1)/ns.CosSen(NumeroX, 0)));//ENVIA EL NUMERO EN RAD Y 1 ES SEN
            } else {
                Res.setText(ns.decimales(ns.CosSen(ns.Disminuir(NumeroX), 1)/ns.CosSen(ns.Disminuir(NumeroX), 0)));//ENVIA EL NUMERO EN RAD Y 1 ES SEN
            }}
        }else{
            if(NumeroX==90){JOptionPane.showMessageDialog(null, "INFINITO");}else{
            NumeroX=NumeroX*(pi/180);
             if(NumeroX < -pi * 2){
                Res.setText(ns.decimales(ns.CosSen(ns.Disminuir(NumeroX), 1)/ns.CosSen(ns.Disminuir(NumeroX), 0)));//ENVIA EL NUMERO EN RAD Y 1 ES SEN
            }else if (pi * 2 >= NumeroX) {//VE SI SE PARAS DE 2PI
                Res.setText(ns.decimales(ns.CosSen(NumeroX, 1)/ns.CosSen(NumeroX, 0)));//ENVIA EL NUMERO EN RAD Y 1 ES SEN
            } else {
                Res.setText(ns.decimales(ns.CosSen(ns.Disminuir(NumeroX), 1)/ns.CosSen(ns.Disminuir(NumeroX), 0)));//ENVIA EL NUMERO EN RAD Y 1 ES SEN
            }}  
        }
            
        
        
        
        Calendar fecha = new GregorianCalendar();
        /**//**/
        String resultado=this.Res.getText()+" "+GR;
        //OPERACION
        int dia = fecha.get(Calendar.DAY_OF_MONTH);
        int mes = fecha.get(Calendar.MONTH);
        int hora = fecha.get(Calendar.HOUR_OF_DAY);
        int minuto = fecha.get(Calendar.MINUTE);
        //IDENTIFICADOR
